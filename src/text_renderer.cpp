// Atlas layout : 95 glyphs × 8 columns = 760 wide,  8 rows tall
static constexpr int ATLAS_COLS   = 95;
static constexpr int GLYPH_W      = 8;
static constexpr int GLYPH_H      = 8;
static constexpr int ATLAS_W      = ATLAS_COLS * GLYPH_W;  // 760
static constexpr int ATLAS_H      = GLYPH_H;               // 8

// ─────────────────────────────────────────────────────────────────────────
TextRenderer::TextRenderer(int w, int h,
                           const char* vertPath,
                           const char* fragPath)
    : winW_(w), winH_(h)
{
    prog_ = createShaderProgram(vertPath, fragPath);

    glGenVertexArrays(1, &vao_);
    glGenBuffers(1, &vbo_);

    glBindVertexArray(vao_);
    glBindBuffer(GL_ARRAY_BUFFER, vbo_);

    // Each vertex: x, y (NDC), u, v  → stride = 4 floats
    const GLsizei stride = 4 * sizeof(float);
    glVertexAttribPointer(0, 2, GL_FLOAT, GL_FALSE, stride, (void*)0);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, stride, (void*)(2*sizeof(float)));
    glEnableVertexAttribArray(1);

    glBindBuffer(GL_ARRAY_BUFFER, 0);
    glBindVertexArray(0);

    buildAtlas();
}

TextRenderer::~TextRenderer()
{
    glDeleteTextures(1, &atlasTexture_);
    glDeleteBuffers(1, &vbo_);
    glDeleteVertexArrays(1, &vao_);
}

// ─────────────────────────────────────────────────────────────────────────
//  buildAtlas  – rasterise the font into a 1-channel GL texture
// ─────────────────────────────────────────────────────────────────────────
void TextRenderer::buildAtlas()
{
    // 1-channel (red) pixel buffer
    std::vector<unsigned char> pixels(ATLAS_W * ATLAS_H, 0);

    for (int g = 0; g < ATLAS_COLS; ++g)
    {
        for (int row = 0; row < GLYPH_H; ++row)
        {
            unsigned char bits = kFont8x8[g][row];
            for (int col = 0; col < GLYPH_W; ++col)
            {
                bool lit = (bits >> (7 - col)) & 1;
                int px   = g * GLYPH_W + col;
                int py   = row;
                pixels[py * ATLAS_W + px] = lit ? 255 : 0;
            }
        }
    }

    glGenTextures(1, &atlasTexture_);
    glBindTexture(GL_TEXTURE_2D, atlasTexture_);

    glPixelStorei(GL_UNPACK_ALIGNMENT, 1);
    glTexImage2D(GL_TEXTURE_2D, 0, GL_RED,
                 ATLAS_W, ATLAS_H, 0,
                 GL_RED, GL_UNSIGNED_BYTE, pixels.data());

    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
    glBindTexture(GL_TEXTURE_2D, 0);
}

// ─────────────────────────────────────────────────────────────────────────
//  draw
// ─────────────────────────────────────────────────────────────────────────
void TextRenderer::draw(const std::string& text,
                        float px, float py,
                        float scale,
                        glm::vec3 color) const
{
    // Pixel dimensions of one glyph at this scale
    const float gw = GLYPH_W * scale;
    const float gh = GLYPH_H * scale;

    std::vector<float> verts;
    verts.reserve(text.size() * 6 * 4);

    float cursorX = px;
    // py is from top-left; convert to bottom-left OpenGL pixel space
    float cursorY = static_cast<float>(winH_) - py - gh;

    for (char c : text)
    {
        int idx = static_cast<int>(c) - 0x20;
        if (idx < 0 || idx >= ATLAS_COLS) { cursorX += gw; continue; }

        // UV for this glyph
        float u0 = static_cast<float>(idx * GLYPH_W)            / ATLAS_W;
        float u1 = static_cast<float>(idx * GLYPH_W + GLYPH_W)  / ATLAS_W;
        float v0 = 0.0f, v1 = 1.0f;

        // NDC conversion
        auto toNDCx = [&](float x){ return 2.0f * x / winW_ - 1.0f; };
        auto toNDCy = [&](float y){ return 2.0f * y / winH_ - 1.0f; };

        float x0 = toNDCx(cursorX),      y0 = toNDCy(cursorY);
        float x1 = toNDCx(cursorX + gw), y1 = toNDCy(cursorY + gh);

        // Two triangles (6 vertices) per glyph
        // tri 1
        verts.insert(verts.end(), {x0,y1, u0,v0});
        verts.insert(verts.end(), {x0,y0, u0,v1});
        verts.insert(verts.end(), {x1,y0, u1,v1});
        // tri 2
        verts.insert(verts.end(), {x0,y1, u0,v0});
        verts.insert(verts.end(), {x1,y0, u1,v1});
        verts.insert(verts.end(), {x1,y1, u1,v0});

        cursorX += gw;
    }

    if (verts.empty()) return;

    // Upload and draw
    glUseProgram(prog_);
    glUniform3fv(glGetUniformLocation(prog_, "uColor"), 1, glm::value_ptr(color));

    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_2D, atlasTexture_);
    glUniform1i(glGetUniformLocation(prog_, "uAtlas"), 0);

    glBindVertexArray(vao_);
    glBindBuffer(GL_ARRAY_BUFFER, vbo_);
    glBufferData(GL_ARRAY_BUFFER,
                 verts.size() * sizeof(float),
                 verts.data(), GL_DYNAMIC_DRAW);

    glEnable(GL_BLEND);
    glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);
    glDrawArrays(GL_TRIANGLES, 0, static_cast<GLsizei>(verts.size() / 4));
    glDisable(GL_BLEND);

    glBindVertexArray(0);
}

